    private void cadastroGames() {

        GameModel gameModel = new GameModel();

        String nome = txtNome.getText();
        String genero = txtGenero.getText();
        String descricao = txtDescricao.getText();
        String dataa = txtData.getText();
        String desenvolvedora = txtDesenvolvedora.getText();
        String plataformas = txtplataformas.getText();
        String Avaliacao = txtAvaliacao.getText();
        String preco = txtPreco.getText();
        String estoque = txtEstoque.getText();

        gameModel.setNome(nome);
        gameModel.setGenero(genero);
        gameModel.setDescricao(descricao);
        gameModel.setData_compra(dataa);
        gameModel.setDesenvolvedora(desenvolvedora);
        gameModel.setPlataformas(plataformas);
        gameModel.setAvaliacao(Avaliacao);
        gameModel.setPreco(Double.valueOf(preco));
        gameModel.setEstoque(Integer.valueOf(estoque));

        if (nome.isEmpty() && genero.isEmpty() && descricao.isEmpty() && dataa.isEmpty() && desenvolvedora.isEmpty() && plataformas.isEmpty() && Avaliacao.isEmpty() && preco.isEmpty() && preco.isEmpty() && estoque.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        } else {
            GameDAO gameDAO = new GameDAO();
            gameDAO.CreateGame(gameModel);

            JOptionPane.showMessageDialog(null, "Jogo cadastrado com sucesso!");

        }
    }

    private void ListarGames() {

        try {

            GameDAO gameDAO = new GameDAO();

            DefaultTableModel tableModel = (DefaultTableModel) tblGames.getModel();

            tableModel.setNumRows(0);

            for (GameModel gameModel : gameDAO.ShowGames()) {

                tableModel.addRow(new Object[]{
                    gameModel.getId_jogo(),
                    gameModel.getNome(),
                    gameModel.getGenero(),
                    gameModel.getDescricao(),
                    gameModel.getData_compra(),
                    gameModel.getDesenvolvedora(),
                    gameModel.getPlataformas(),
                    gameModel.getAvaliacao(),
                    gameModel.getPreco(),
                    gameModel.getEstoque()
                });

                gameDAO.ShowGames();

            }

        } catch (Exception e) {

            System.out.println("erro: " + e);

        }

    }

    private void AtualizarGame() {

        int id;
        String nome, genero, descricao, data, desenvolvedora, plataformas, Avaliacao, preco, estoque;

        id = Integer.parseInt(txtID.getText());
        nome = txtNome.getText();

        genero = txtGenero.getText();
        descricao = txtDescricao.getText();
        data = txtData.getText();
        desenvolvedora = txtDesenvolvedora.getText();
        plataformas = txtplataformas.getText();
        Avaliacao = txtAvaliacao.getText();
        preco = txtPreco.getText();
        estoque = txtEstoque.getText();

        GameModel gameModel = new GameModel();

        gameModel.setId_jogo(id);
        gameModel.setNome(nome);
        gameModel.setGenero(genero);
        gameModel.setDescricao(descricao);
        gameModel.setData_compra(data);
        gameModel.setDesenvolvedora(desenvolvedora);
        gameModel.setPlataformas(plataformas);
        gameModel.setAvaliacao(Avaliacao);
        gameModel.setPreco(Double.valueOf(preco));
        gameModel.setEstoque(Integer.parseInt(estoque));

        GameDAO gameDAO = new GameDAO();

        gameDAO.UpdateGame(gameModel);

    }

    private void ExcluirGame() {

        try {

            int id = Integer.parseInt(txtID.getText());

            GameModel gameModel = new GameModel();

            gameModel.setId_jogo(id);

            GameDAO gameDAO = new GameDAO();
            gameDAO.DeleteGame(id);

            JOptionPane.showMessageDialog(null, "Jogo deletado com sucesso!");

        } catch (NumberFormatException e) {
            System.out.println("O texto em txtID não é um número válido.");
        }
    }

