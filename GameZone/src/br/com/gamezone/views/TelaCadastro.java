            txtGenero.setText(tblGames.getModel().getValueAt(carregar, 2).toString());
            txtDescricao.setText(tblGames.getModel().getValueAt(carregar, 3).toString());
            txtData.setText(tblGames.getModel().getValueAt(carregar, 4).toString());
            txtDesenvolvedora.setText(tblGames.getModel().getValueAt(carregar, 5).toString());
